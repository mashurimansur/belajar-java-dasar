public class Block {
    public static void main(String[] args) { //ini adalah blok, yaitu dibuka denga kurung kurawal dan diakhiri kurung kurawal
        System.out.println("Hello World 1"); //ini adalah statement
        System.out.println("Hello World 2");
        System.out.println("Hello World 3");
        { //ini juga blok
            System.out.println("Hello World 4");
            System.out.println("Hello World 5");
            System.out.println("Hello World 6");
        }
    }
}
